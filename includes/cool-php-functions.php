function coldiff($R1,$G1,$B1,$R2,$G2,$B2){
    return max($R1,$R2) - min($R1,$R2) +
           max($G1,$G2) - min($G1,$G2) +
           max($B1,$B2) - min($B1,$B2);
}

function getRgbTotal( $color ){
    $color = hex2rgb( $color ); 
    return array_sum($color);  
}


